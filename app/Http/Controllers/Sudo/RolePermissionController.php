     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $rol)
    {
        $rol = Role::find($rol);

        $rol->syncPermissions($request->permisos);
        
        return redirect()->route('rolespermission.edit', $rol)->with(['info' => 'Permisos asugnados al rol']);
    }

